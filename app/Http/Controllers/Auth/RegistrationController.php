    public function index()
    {
        $search = request('search', '');
        $perPage = request('perPage', 10);
        return User::leftJoin('cs_company_master','cs_company_master.company_id','=','users.company_id')
        ->leftJoin('cs_company_branch_unit','cs_company_branch_unit.branch_id','users.branch_id')
        ->leftJoin('cs_users_roles','cs_users_roles.id','users.role_id')
        ->where('name', 'like', "%{$search}%")
        ->paginate($perPage);
    }

